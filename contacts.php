<div align="justify">Le COPIL SIG &quot;DRAAF, DREAL, DDTM 27 et 76&quot; de 2012 a mandaté 
  un groupe de travail pour étudier la faisabilité de l'OS Communale, un outil 
  conçu à partir des bases de données existantes et permettant d’avoir une vision 
  de l’occupation des sols et plus tard une approche de l’évolution de la consommation 
  de l’espace. </div>
<p align="justify">Une équipe conception a été constituée entre la DRAAF et la 
  DDTM 76 pour fabriquer une maquette de cet outil qui a été validée au COPIL 
  SIG de décembre 2013.</p>
